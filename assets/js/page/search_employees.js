function showHideDetail() {
    var x = document.querySelector("#btn-showHideDetail a");
    var detail = document.querySelector('#detailSearch')

    var winLoc = window.location.href;
    if (winLoc.indexOf('_tr') > -1) {
        if (x.innerHTML === 'Daha Fazla Ayrıntı Ara <i class="lni lni-chevron-down"></i>') {
            x.innerHTML = 'Detayı Gizle <i class="lni lni-chevron-up"></i>';
            setTimeout( function(){ detail.style.display = 'block'; }, 300 );
        } else {
            x.innerHTML = 'Daha Fazla Ayrıntı Ara <i class="lni lni-chevron-down"></i>';
            setTimeout( function(){ detail.style.display = 'none'; }, 300 );        
        }
    } else {
        if (x.innerHTML === 'Search More Detail <i class="lni lni-chevron-down"></i>') {
            x.innerHTML = 'Hide Detail <i class="lni lni-chevron-up"></i>';
            setTimeout( function(){ detail.style.display = 'block'; }, 300 );
        } else {
            x.innerHTML = 'Search More Detail <i class="lni lni-chevron-down"></i>';
            setTimeout( function(){ detail.style.display = 'none'; }, 300 );        
        }
    }
}

function prevPage(page){
    var keywords = document.getElementById("intro-keywords").value.toLowerCase();
    var location = document.getElementById("search-location").value.toLowerCase();
    var lastEducation = document.getElementById("last-education").value.toLowerCase();
    var lastCompany = document.getElementById("last-company").value.toLowerCase();
    var birthDateFrom = document.getElementById("dateBirth").value.split(" - ")[0];
    var birthDateTo = document.getElementById("dateBirth").value.split(" - ")[1] == undefined ? '' : document.getElementById("dateBirth").value.split(" - ")[1];
    var skillLanguage = document.getElementById("skill-language").value.toLowerCase();

    if (keywords !== "" || location !== "" || lastEducation !== "" || lastCompany !== "" || birthDateFrom !== "" || birthDateTo !== "" || skillLanguage !== "" ) {
        if (page > 1) {
            page--;
            searchEmployees(page);
        }
    } else {
        if (page > 1) {
            page--;
            searchEmployeesView(page);
        }
    }
    
}

