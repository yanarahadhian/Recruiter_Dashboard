function openPage(pageName, elmnt) {
    var i, tabcontent, navtabs;
    tabcontent = document.getElementsByClassName("tabcontent");
    for (i = 0; i < tabcontent.length; i++) {
        tabcontent[i].style.display = "none";
    }
    navtabs = document.getElementsByClassName("nav-tab");
    for (i = 0; i < navtabs.length; i++) {
        navtabs[i].classList.remove("nav-active");
    }

    if (elmnt.parentElement.classList.contains('nav-active')) {
        elmnt.parentElement.classList.remove('nav-active');
    } else {
        elmnt.parentElement.classList.add("nav-active");
    }
    document.getElementById(pageName).style.display = "block";
}

